#include<iostream>
using namespace std;
int main()
{
    int a,b,sumation,substraction,multiplication,division;
    cout<<"Enter the first number: ";
    cin>>a;
    cout<<"Enter the second number: ";
    cin>>b;
    sumation=a+b;
    substraction=a-b;
    multiplication=a*b;
    division=a/b;
    cout<<"The summation of two number is :"<<sumation<<endl;
    cout<<"The substraction of two number is :"<<substraction<<endl;
    cout<<"The multiplicatio of two number is :"<<multiplication<<endl;
    cout<<"The division of two number is :"<<division<<endl;
return 0;
}
